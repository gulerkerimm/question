public class typeCasting {
    public static void main(String[] args) {


        //1- short data tipinde bir değişken oluşturunuz ve onu bir int değişkenine dönüştürünüz.

        short yas =12;
        int yeniYas =(int) yas ;
        System.out.println(yeniYas);

        //2- Long data tipinde bir değişkenin oluşturunuz ve onu bir int değişkeninde dönüştürünüz.

        long yas1 =12L;
        int yeniYas1=(int)yas1;
        System.out.println(yeniYas1);

        //3-Double data tipinde bir değişken oluşturunuz ve onu bir float değişkenine dönüştürünüz.

        double  fiyat =2.34;
        float yeniFiyat=(float) fiyat;
        System.out.println(yeniFiyat);

        //4-Double data tipinde bir değişken oluşturunuz ve onu bir short değişkenine dönüştürünüz.
        //sonrasında bu short değişkeninin değeri konsolda yazdırınız.

        double fiyat2=1.23;
        short yeniFiyat2=(short)fiyat2;
        System.out.println(yeniFiyat2);

        //Byte data tipinde bir değişkeb oluşturnuz ve onu bir double değişkenşne dönüştürünüz.
        //Sonrasında bu double değişkenin değeri konsolda yazdırınız.

        byte age=23;
        double newAge=(double) age ;
        System.out.println(newAge);













    }
}
